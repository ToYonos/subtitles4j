package info.toyonos.subtitles4j.factory;

public class SubtitlesGenerationException extends Subtitles4jException
{
	private static final long serialVersionUID = 4714051403733532699L;

	public SubtitlesGenerationException()
	{
		super();
	}

	public SubtitlesGenerationException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public SubtitlesGenerationException(String message)
	{
		super(message);
	}

	public SubtitlesGenerationException(Throwable cause)
	{
		super(cause);
	}
}
